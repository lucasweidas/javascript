// Objeto Math

const valor = 7.5;

console.log(Math.round(valor)); // Arredonda para cima e para baixo
console.log(Math.floor(valor)); // Arredonda sempre para baixo
console.log(Math.ceil(valor)); // Arredonda sempre para cima
console.log(Math.trunc(valor)); // Retorna a parte inteira do valor

// Números aleatórios
const random = Math.random();
console.log(random)
console.log(Math.round(random * 100));
